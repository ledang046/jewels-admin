<?php

namespace App\Http\Controllers;

use App\Models\Coupon;
use Illuminate\Http\Request;

class CouponController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data = Coupon::orderBy("id","asc")
            ->paginate(5);
            
        return view('backend.coupons_read', ["data" => $data]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $action = route('coupons.store');
        return view('backend.coupons_create_update', ['action' => $action]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();

        $coupon = new Coupon;

        $coupon->name = $data['name'];
        $coupon->number = $data['number'];
        $coupon->code = $data['code'];
        $coupon->time = $data['time'];
        $coupon->condition = $data['condition'];
        $coupon->save();

        return redirect(route('coupons.index')); 
    }

